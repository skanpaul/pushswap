#include "push_swap.h"

/* ************************************************************************** */
t_ps	*ps_get_last_elem(t_ps **ptr_top)
{
	t_ps	*ptr;

	ptr = *ptr_top;
	if (ptr == NULL)
		return (NULL);
	while (ptr->next != NULL)
		ptr = ptr->next;
	return (ptr);
}
