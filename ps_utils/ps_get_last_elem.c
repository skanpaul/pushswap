t_elem	*ll_get_last_elem(t_elem *top_elem)
{
	t_elem	*ptr;

	ptr = top_elem;
	if (ptr == NULL)
		return (NULL);
	while (ptr->next != NULL)
		ptr = ptr->next;
	return (ptr);
}
