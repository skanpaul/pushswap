size_t	ft_printf_fd_s(char *s, int fd)
{
	size_t	len;

	len = ft_strlen(s);
	if (fd == -1)
		return (0);
	if (!s)
	{
		if (write(fd, "(null)", 6) == -1)
			return (0);
		return (6);
	}
	if (write(fd, s, len) == -1)
		return (0);
	return (len);
}
