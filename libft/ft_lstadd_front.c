/* ************************************************************************** */
void	ft_lstadd_front(t_list **alst, t_list *new)
{
	if (alst == NULL)
		return ;
	if (*alst == NULL)
		*alst = new;
	else
	{
		(*new).next = (*alst);
		(*alst) = new;
	}
	return ;
}
