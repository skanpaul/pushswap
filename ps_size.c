int	ps_size(t_ps *top_elem)
{
	int		size;
	t_ps	*ptr;

	ptr = top_elem;
	if (!ptr)
		return (0);
	size = 0;
	while (ptr != NULL)
	{
		ptr = ptr->next;
		size++;
	}
	return (size);
}


