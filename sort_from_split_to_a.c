void	sort_from_split_to_a(t_data *d)
{
	t_sta	sta_d;

	sta_d = split_to_a(d);
	sort_sub_chunk_a(d);
	sort_sub_rot_chunk_a(d, sta_d.cnt_rev_rot);
	sort_sub_chunk_b_group(d, sta_d.sub_group_b_id);
}

